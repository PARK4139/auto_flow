def 메모_조회(memo_object):
    """
    함수나 클래스 같은 Python 객체를 입력받아
    그 객체의 소스 코드를 문자열로 반환하고 출력합니다.
    """
    try:
        # inspect.getsource()를 사용하여 객체의 소스 코드를 가져옵니다.
        source_code = inspect.getsource(memo_object)
        
        # textwrap.dedent()를 사용하여 코드 앞의 공통 들여쓰기를 제거합니다.
        dedented_code = textwrap.dedent(source_code)
        
        print(f"--- '{memo_object.__name__}' 메모 내용 ---")
        print(dedented_code)
        return dedented_code
    except (TypeError, OSError) as e:
        error_message = f"'{memo_object.__name__}'의 소스 코드를 가져오는 데 실패했습니다: {e}"
        print(error_message)
        return error_message
